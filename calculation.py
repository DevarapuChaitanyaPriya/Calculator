

def add(n1,n2):
    """
    takes 2 parameters and return the sum of two numbers.
    """
    return n1+n2 


def sub(n1,n2):
    """
    takes 2 parameters and return the subraction of two numbers.
    """
    return n1-n2  

def mul(n1,n2):
    """
    takes 2 parameters and return the multiplication of two numbers.
    """
    return n1*n2

def div(n1,n2):
    """
    takes 2 parameters and return the division of two numbers.
    """
    return n1/n2 


